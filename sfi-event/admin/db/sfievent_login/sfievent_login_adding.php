<?php
/**********************************************************************************************/
// 引入模組檔
/**********************************************************************************************/
include ("../check.php");

$_table = TABLE_LOGIN;

/**********************************************************************************************/
// 檢查空白欄位
/**********************************************************************************************/
CheckEmpty($_POST[$_table.'ID'], "管理帳號");

/**********************************************************************************************/
// 處理特別欄位
/**********************************************************************************************/
$loginAccess = "";
for ($i = 0; $i < count($_POST[$_table.'Access']); $i++) {
	$loginAccess .= $_POST[$_table.'Access'][$i] . ",";
}
$loginAccess = substr($loginAccess, 0, -1);

/**********************************************************************************************/
// 存入資料庫 or 更新資料庫
/**********************************************************************************************/
$DB->Select(DATABASE);
$DB->Query("INSERT INTO ". $_table ." (".$_table."ID, ".$_table."PW, ".$_table."Email, ".$_table."Access)VALUES('". $_POST[$_table.'ID'] ."','". $_POST[$_table.'PW'] ."','". $_POST[$_table.'Email'] ."','". $loginAccess ."');");
$DB->Log("新增資料 - " . $_table, $_COOKIE['loginID']);

/**********************************************************************************************/
// 輸出成功訊息
/**********************************************************************************************/
Msg("新增完成！", $_table . "_list.php");
?>