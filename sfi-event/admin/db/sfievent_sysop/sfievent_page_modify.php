<?php
/**********************************************************************************************/
// 引入模組檔
/**********************************************************************************************/
include ("../check.php");

$_table = TABLE_PAGE;

/**********************************************************************************************/
// 檢查空白欄位
/**********************************************************************************************/
// none

/**********************************************************************************************/
// 存入資料庫 or 更新資料庫
/**********************************************************************************************/
$DB->Select(DATABASE);

$pageQuery = $DB->Query("SELECT * FROM ". $_table .";");
while ($pageArray = $DB->Arrays($pageQuery))
{
	$disabled = ($_POST['pageDisabled_' . $pageArray[$_table.'No']])? 1 : 0;
	$DB->Query("UPDATE ". $_table ." SET ".$_table."Disabled = '". $disabled ."', ".$_table."Sort = '". $_POST['pageSort_' . $pageArray[$_table.'No']] ."', ".$_table."Name = '". $_POST['pageName_' . $pageArray[$_table.'No']] ."', ".$_table."Intro = '". $_POST['pageIntro_' . $pageArray[$_table.'No']] ."' WHERE ".$_table."No = '". $pageArray[$_table.'No'] ."';");
}

$DB->Log("更改頁面資訊及排序", $_COOKIE['loginID']);

/**********************************************************************************************/
// 輸出成功訊息
/**********************************************************************************************/
Msg("更改頁面資訊及排序變更完成！", "{$_table}_list.php");
?>