function cityChange(cityObj, zoneObj)
{
	i = cityObj.selectedIndex;
	zoneObj.length = cityarea_account[i] - cityarea_account[i-1];
	index = cityarea_account[i-1] + 1;
	if (i == 0)
	{
		zoneObj.length = 1;
		zoneObj.options[0].value = "請選擇";
		zoneObj.options[0].text = "請選擇";
		zoneObj.options[0].selected = true;
		return false;
	}
	for (j = 0; j < zoneObj.length; j ++)
	{
		zoneObj.options[j].value = index + j;
		zoneObj.options[j].text = cityarea[index + j];
	}
	zoneObj.options[0].selected = true;
}
